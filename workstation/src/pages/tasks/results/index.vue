<template>
  <el-table
    class="clickable"
    :data="data"
    @row-click="rowClick"
    @sort-change="sortChange"
  >
    <column-default
      :label="$t('results.column.taskName')"
      prop="taskName"
      sortable="custom"
      :width="240"
    />
    <column-default
      :label="$t('results.column.subTaskName')"
      prop="subTaskName"
      sortable="custom"
    />
    <column-status
      :label="$t('results.column.status')"
      prop="conditions"
      :statusList="taskConditions"
      :width="120"
    />
    <column-user
      :label="$t('results.column.reporter')"
      prop="workerUUID"
      nameProp="workerName"
      imageProp="workerProfile"
      type="tooltip"
      :width="70"
    />
    <column-date
      :label="$t('results.column.reportedDate')"
      type="time"
      prop="reportedDate"
      :width="150"
    />
    <template slot="empty">
      <img src="~assets/images/empty/img-common-empty.jpg" />
      <p>{{ $t('home.reportList.empty') }}</p>
    </template>
  </el-table>
</template>

<script>
import columnMixin from '@/mixins/columns'
import { conditions as taskConditions } from '@/models/task/Task'

export default {
  mixins: [columnMixin],
  props: {
    data: Array,
  },
  data() {
    return {
      taskConditions,
    }
  },
  methods: {
    sortChange(params) {
      this.$emit('sort-change', params)
    },
    rowClick(row) {
      this.$router.push(`/tasks/${row.taskId}/${row.subTaskId}`)
    },
  },
}
</script>
