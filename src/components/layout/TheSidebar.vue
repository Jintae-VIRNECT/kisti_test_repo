<template>
  <nav class="the-sidebar">
    <div
      class="the-sidebar__inner"
      :class="{ 'the-sidebar__inner--wide': showWide }"
    >
      <!-- 일반 -->
      <div class="the-sidebar__logo">
        <a @click.stop="toggleWide" />
      </div>
      <div class="the-sidebar__upper">
        <LayoutTheSidebarMenuList
          :menus="menus"
          :isWide="showWide"
          @openCollapse="openCollapse"
          @closeCollapse="closeCollapse"
        />
      </div>
      <!-- 워크스페이스 설정 -->
      <div class="the-sidebar__lower" v-if="!hideWorkspaceSetting">
        <LayoutTheSidebarMenuList
          :menus="bottomMenus"
          :isWide="showWide"
          @openCollapse="openCollapse"
          @closeCollapse="closeCollapse"
        />
      </div>
    </div>
    <transition name="collapse">
      <div v-if="showCollapse" class="the-sidebar__collapse" @click.stop>
        <keep-alive>
          <component :is="collapseComponent" @closeCollapse="closeCollapse" />
        </keep-alive>
      </div>
    </transition>
  </nav>
</template>

<script>
import collapseTask from '@/components/layout/collapses/TheSidebarCollapseTask'
import collapseMember from '@/components/layout/collapses/TheSidebarCollapseMember'
import collapseContents from '@/components/layout/collapses/TheSidebarCollapseContents'
import collapseProjects from '@/components/layout/collapses/TheSidebarCollapseProjects'
import collapseSettings from '@/components/layout/collapses/TheSidebarCollapseSettings'

import { mapGetters } from 'vuex'
import filterMixin from '@/mixins/filters'

export default {
  mixins: [filterMixin],
  components: {
    collapseTask,
    collapseMember,
    collapseContents,
    collapseProjects,
    collapseSettings,
  },
  props: {
    menus: Array,
    bottomMenus: Array,
  },
  data() {
    return {
      showCollapse: false,
      showWide: false,
      collapseComponent: null,
      logoCollapse: 'collapseWorkspace',
    }
  },
  computed: {
    ...mapGetters({
      activeWorkspace: 'auth/activeWorkspace',
    }),
    hideWorkspaceSetting() {
      return this.$isOnpremise && this.activeWorkspace.role !== 'MASTER'
    },
  },
  methods: {
    toggleWide() {
      this.showWide = !this.showWide
    },
    openCollapse(component) {
      this.showWide = false
      if (this.showCollapse && this.collapseComponent === component) {
        this.showCollapse = false
      } else {
        this.collapseComponent = component
        this.showCollapse = true
      }
    },
    closeCollapse() {
      this.showWide = false
      this.showCollapse = false
    },
  },
  mounted() {
    document.addEventListener('click', this.closeCollapse)
  },
}
</script>

