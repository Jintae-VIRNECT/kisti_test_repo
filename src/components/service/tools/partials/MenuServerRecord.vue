<template>
  <menu-button
    text="녹화"
    :active="isRecording"
    :disabled="disabled"
    :src="require('assets/image/ic_record_off.svg')"
    :icActive="isRecording"
    :activeSrc="require('assets/image/ic_record_ing.svg')"
    @click="recording"
  ></menu-button>
</template>

<script>
import toolMixin from './toolMixin'
import betaCheckMixin from 'mixins/betaCheck'
export default {
  name: 'ServerRecordMenu',
  mixins: [toolMixin, betaCheckMixin],
  data() {
    return {
      isRecording: false,
    }
  },
  computed: {},
  watch: {},
  methods: {
    recording() {
      if (this.disabled) return
      if (this.checkBeta()) {
        return false
      }
      if (!this.isRecording) {
        this.record()
        this.$eventBus.$emit('serverRecord', true)
      } else {
        this.stop()
        this.$eventBus.$emit('serverRecord', false)
      }
    },
    record() {
      this.isRecording = true
    },
    stop() {
      this.isRecording = false
    },
  },

  /* Lifecycles */
  beforeDestroy() {},
  mounted() {},
}
</script>
